using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace Math
{
    public class Subtractor
    {
        public int sub(int x, int y)
        {
            return x - y;
        }

        public float sub(float x, float y)
        {
            return x - y;
        }
    }
}
